#include <iostream>

using namespace std;

int main()
{
	int one = 1;
	int two = 2;
	int three = 3;
	int four = 4;
	//값을 변수에 저장

	cout << "1 + 3 = " << one + three << endl;
	cout << "4 - 2 = " << four - two << endl;
	cout << "2 * 3 = " << two * three << endl;
	cout << "4 / 2 = " << four / two << endl;
	//문자열 출력 후 변수를 계산하여 값 출력

	cout << "2 + 3 * 4 = " << two + three * four << endl;
	cout << "2 + 3 * 4 = " << two + three * four << endl;
	cout << "1 + 4  / 2 = " << one * four / two << endl;
	//문자열 출력 후 변수를 계산하여 값 출력

	return 0;
}